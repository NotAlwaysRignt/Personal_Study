//详见高德api官网，“参考手册”、“定位”
map = new AMap.Map('container',{
        resizeEnable: true
    });
    
map.plugin('AMap.Geolocation', function () {
    geolocation = new AMap.Geolocation({
        enableHighAccuracy: true,//是否使用高精度定位，默认:true
        timeout: 10000,          //超过10秒后停止定位，默认：无穷大
        maximumAge: 0,           //定位结果缓存0毫秒，默认：0
        convert: true,           //自动偏移坐标，偏移后的坐标为高德坐标，默认：true
        showButton: true,        //显示定位按钮，默认：true
        buttonPosition: 'LB',    //定位按钮停靠位置，默认：'LB'，左下角
        buttonOffset: new AMap.Pixel(10, 20),//定位按钮与设置的停靠位置的偏移量，默认：Pixel(10, 20)
        showMarker: true,        //定位成功后在定位到的位置显示点标记，默认：true
        showCircle: true,        //定位成功后用圆圈表示定位精度范围，默认：true
        panToLocation: true,     //定位成功后将 AMap.event.addListener(geolocation, 'error', onError);      //返回定位出错信息定位到的位置作为地图中心点，默认：true
        zoomToAccuracy:true      //定位成功后调整地图视野范围使定位位置及精度范围视野内可见，默认：false
    });
    map.addControl(geolocation);
    geolocation.getCurrentPosition();
    AMap.event.addListener(geolocation, 'complete', onComplete);//返回定位信息
    AMap.event.addListener(geolocation, 'error', onError);      //返回定位出错信息
});
    /*
    AMap.event.addListener(geolocation, 'complete', onComplete);
    onComplete由自己定义，定位成功时触发此事件,当调用这个onComplete回调函数时
    会传入一个参数，这个参数为GeolocationResult对象
    */
    function onComplete(result){
        //result即为GeolocationResult对象
        console.log("success");
        console.log(result.formattedAddress);
        console.log(result.message);
    }
    function onError(e){
        console.log(e.info);
        console.log(e.message);
    }

